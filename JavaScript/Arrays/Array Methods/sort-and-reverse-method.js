let arrStr = ["Hussain", "Asad", "Suleman", "Hamid"];
let arrNum = [10, 20, 50, 30];
document.write(arrStr + "<br>");
document.write(arrNum + "<br>");

// sort()
// Arrange arrays elements.
arrStr.sort();
arrNum.sort();
document.write(arrStr + "<br>");
document.write(arrNum + "<br>");

// reverse()
// reverse the order of arrays elements.
arrStr.reverse();
arrNum.reverse();
document.write(arrStr + "<br>");
document.write(arrNum + "<br>");