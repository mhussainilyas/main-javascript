// ======================
//   Logical Operators
// ======================

let age = 19;

// 1. Logical AND (&&)
if (age >= 11 && age <= 18) {
  console.log("You are eligible.");
}

// 2. Logical OR (||)
if (age >= 11 || age <= 18) {
  console.log("You are eligible.");
}

// 3. Logical NOT (!)
if (!(age != 19)) {
  console.log("Your age is 19.");
}
